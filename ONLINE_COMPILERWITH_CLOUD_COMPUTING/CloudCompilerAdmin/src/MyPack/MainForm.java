    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        // TODO add your handling code here:
        int index = jList1.getSelectedIndex();
        if(index == -1) return;
        if(JOptionPane.showConfirmDialog(this, "Are you sure you wish to delete this record?") == JOptionPane.YES_OPTION){
            userdb.taskList.removeElementAt(index);
            WriteToFile();
            readFromFile();
            fillTaskList();
        }
    }//GEN-LAST:event_jButton19ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SingleTask st = new SingleTask();
        st.title = jTextField1.getText();
        st.text = jTextArea1.getText();
        st.c = Calendar.getInstance();
        userdb.taskList.addElement(st);
        WriteToFile();
        readFromFile();
        fillTaskList();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        int index = jList1.getSelectedIndex();
        if(index == -1) return;
        String op = "TITLE : " + userdb.taskList.elementAt(index).title + "\n";
        String dt = userdb.taskList.elementAt(index).c.get(Calendar.DAY_OF_MONTH) + "/" + userdb.taskList.elementAt(index).c.get(Calendar.MONTH) + "/" + userdb.taskList.elementAt(index).c.get(Calendar.YEAR);
        op += "DATED : " + dt + "\n";
        op += "TASK : " + userdb.taskList.elementAt(index).text;
        jTextArea2.setText(op);
    }//GEN-LAST:event_jList1ValueChanged

