    void writePath(){
        try{
            BufferedWriter bw = new BufferedWriter(new FileWriter("c:\\projectData\\5040DB\\paths.dat"));
            bw.write(jTextField2.getText() + "\n");
            bw.write(jTextField3.getText() + "\n");
            bw.close();
        }catch(Exception e){
            System.out.println("Error : " + e);
        }
    }

    void readPath(){
        try{
            BufferedReader bw = new BufferedReader(new FileReader("c:\\projectData\\5040DB\\paths.dat"));
            jTextField2.setText(bw.readLine());
            jTextField3.setText(bw.readLine());
            bw.close();
        }catch(Exception e){
            System.out.println("Error : " + e);
        }
    }

    
    void fillTaskList(){
        DefaultListModel lm = new DefaultListModel();
        for(SingleTask s : userdb.taskList){
            lm.addElement(s.title);
        }
        jList1.setModel(lm);
    }
    
   
    
